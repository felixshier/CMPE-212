
public class IllegalFastener extends Exception {
	
	/**
	 * Exception class to throw in the case of an 
	 * illegal parameter in the fastener hierarchy
	 */
	private static final long serialVersionUID = 1L;
	
	// IllegalFaster exception 
	public IllegalFastener(String message) {
		super(message);
	} // end IllegalFastener

} // end IllegalFastener class
